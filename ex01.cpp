#include <bits/stdc++.h>
using namespace std;

int main() {
    int *ptr1;
    char *ptr2;
    
    // Finding size using sizeof()
    cout << sizeof(ptr1) << endl;
    cout << sizeof(ptr2) << endl;

    double i;
    cout <<sizeof(i) << endl;

    int x;
    cout << sizeof(x) << endl;
    
    return 0;
}